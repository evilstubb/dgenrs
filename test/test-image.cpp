TEST(Image, RandomPNG) {
  const unsigned dim = 64;
  Image i1(ImageType::Luminance, dim, dim);
  {
    std::random_device rand;
    std::mt19937 prand(rand());
    std::uniform_int_distribution<uint8_t> distribution(0, 255);
    for (unsigned i = 0; i < dim; i++)
      for (unsigned j = 0; j < dim; j++)
        i1.pixel(i, j)[0] = distribution(prand);
  }
  std::ostringstream os;
  i1.write_png(os);
  std::istringstream is(os.str());
  Image i2 = Image::read_png(is);
  ASSERT_EQ(i1.kind(), i2.kind());
  ASSERT_EQ(i1.width(), i2.width());
  ASSERT_EQ(i1.height(), i2.height());
  for (unsigned i = 0; i < dim; i++)
    for (unsigned j = 0; j < dim; j++)
      ASSERT_EQ(i1.pixel(i, j)[0], i2.pixel(i, j)[0]);
}

